/**
 * Get all investor keywords
 */
export function getAllInvestorKeywords(): string[] {
  const allKeywords: string[] = [];

  Object.entries(InvestorKeywords).forEach(([_, keywords]) => {
    if (Array.isArray(keywords)) {
      allKeywords.push(...keywords);
    } else if (typeof keywords === 'object') {
      Object.values(keywords).forEach((kw) => {
        if (Array.isArray(kw)) {
          allKeywords.push(...kw);
        }
      });
    }
  });

  return [...new Set(allKeywords)];
}

/**
 * Get investor keyword statistics
 */
export function getInvestorKeywordStats(): Record<string, number> {
  const stats: Record<string, number> = {};

  Object.entries(InvestorKeywords).forEach(([category, keywords]) => {
    if (Array.isArray(keywords)) {
      stats[category] = keywords.length;
    } else if (typeof keywords === 'object') {
      let count = 0;
      Object.values(keywords).forEach((kw) => {
        if (Array.isArray(kw)) {
          count += kw.length;
        }
      });
      stats[category] = count;
    }
  });

  stats.total = getAllInvestorKeywords().length;
  return stats;
}

export const investorKeywords = new (class {
  getAll() {
    return getAllInvestorKeywords();
  }

  getByCategory(category: keyof typeof InvestorKeywords) {
    const keywords = InvestorKeywords[category];
    if (Array.isArray(keywords)) {
      return keywords;
    } else if (typeof keywords === 'object') {
      const result: string[] = [];
      Object.values(keywords).forEach((kw) => {
        if (Array.isArray(kw)) {
          result.push(...kw);
        }
      });
      return result;
    }
    return [];
  }

  getStats() {
    return getInvestorKeywordStats();
  }

  findInvestorProfile(keywords: string[]): string[] {
    // Match investor characteristics from keywords
    const profiles: string[] = [];
    const keywordLower = keywords.map((k) => k.toLowerCase());

    if (
      keywordLower.some((k) =>
        ['cash', 'all cash', 'cash buyer'].some((c) =>
          k.includes(c.toLowerCase())
        )
      )
    ) {
      profiles.push('cash_buyer');
    }

    if (
      keywordLower.some((k) =>
        ['fix and flip', 'flipper', 'flipping'].some((f) =>
          k.includes(f.toLowerCase())
        )
      )
    ) {
      profiles.push('fix_and_flip');
    }

    if (
      keywordLower.some((k) =>
        ['buy and hold', 'rental', 'landlord'].some((b) =>
          k.includes(b.toLowerCase())
        )
      )
    ) {
      profiles.push('buy_and_hold');
    }

    if (keywordLower.some((k) => k.includes('syndication'))) {
      profiles.push('syndication');
    }

    return profiles;
  }
})();
