/**
 * Get all commercial/industrial keywords
 */
export function getAllCommercialKeywords(): string[] {
  const allKeywords: string[] = [];

  Object.entries(CommercialIndustrialKeywords).forEach(([_, keywords]) => {
    if (Array.isArray(keywords)) {
      allKeywords.push(...keywords);
    } else if (typeof keywords === 'object') {
      Object.values(keywords).forEach((kw) => {
        if (Array.isArray(kw)) {
          allKeywords.push(...kw);
        }
      });
    }
  });

  return [...new Set(allKeywords)];
}

/**
 * Get keyword statistics
 */
export function getCommercialKeywordStats(): Record<string, number> {
  const stats: Record<string, number> = {};

  Object.entries(CommercialIndustrialKeywords).forEach(
    ([category, keywords]) => {
      if (Array.isArray(keywords)) {
        stats[category] = keywords.length;
      } else if (typeof keywords === 'object') {
        let count = 0;
        Object.values(keywords).forEach((kw) => {
          if (Array.isArray(kw)) {
            count += kw.length;
          }
        });
        stats[category] = count;
      }
    }
  );

  stats.total = getAllCommercialKeywords().length;
  return stats;
}

